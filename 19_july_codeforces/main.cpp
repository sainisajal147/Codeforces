void bin(vector<int> arr,int key,int l,int r,int &ans)
{
     if (r >= l) {
        int mid = l + (r - l) / 2;
        if (arr[mid] == key)
            ans=mid;
        if (arr[mid] > key)
            bin(arr,key, l, mid - 1, ans);
        bin(arr,key, mid + 1, r, ans);
    }
}

